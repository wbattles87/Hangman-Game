// let allows you to declare variables that are limited in scope to the block, statement, or expression on which it is used. 
// This is unlike the var keyword, which defines a variable globally, or locally to an entire function regardless of block scope.

// setting variable randNum equal to a random generator for an array//
let randNum = Math.floor(Math.random() * word.length);
// now setting choosenWord to word which now has the random word generated from above//
let choosenWord = word[randNum];
// this is making the new array for the underscore we will have generated by the length of the word we randomly picked
let underScore = [];
let rightWord = [];
let wrongWord = [];

//DOM Manipulation
let docUnderScore = document.getElementsByClassName("underscore");
let docRightGuess = document.getElementsByClassName("rightGuess");
let docWrongGuess = document.getElementsByClassName("wrongGuess");



console.log(choosenWord);
// this will generate the amount of underscores needed per choosen word
// arrow function used here to keep the scope in check
// easier to see the scope of the function

let generateUnderscore = () => {
	for(let i = 0; i < choosenWord.length; i++){
			
			// this is essentially adding _ to a blank array we are creating with the amount equal to the word we randomly picked
			underScore.push("_");			
		}
		return underScore;
}


// this will capture the users input, in this case the pressing of a key
document.addEventListener("keypress", (event) => {					
	let keyword = String.fromCharCode(event.keyCode);				// this gets the Unicode value and converts that value into a character
	if(choosenWord.indexOf(keyword) > -1) {							// if the user guesses the right letter, 
		rightWord.push(keyword);									// adds the correct guess to the rightWord array
		underScore[choosenWord.indexOf(keyword)] = keyword; 		// this replaces the underscore in the word area with the correct letter guessed
		docUnderScore[0].innerHTML = underScore.join(" ");
		docRightGuess[0].innerHTML = rightWord;
															
		if(underScore.join(" ") == choosenWord) {					// join puts together the elements of an array and puts them together as a string
			alert("You Win");		
		}
	}
	else {
		wrongWord.push(keyword);							// Puts the wrong letter guessed into the wrongGuess array
		docWrongGuess[0].innerHTML = wrongWord;							
	}

});

docUnderScore[0].innerHTML = generateUnderscore().join(" ");
